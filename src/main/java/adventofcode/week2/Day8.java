	private static final String BIGGER = ">";

	private static final String BIGGER_EQUAL = ">=";

	private static final String EQUAL = "==";

	private static final String NOT_EQUAL = "!=";

	private static final String SMALLER = "<";

	private static final String SMALLER_EQUAL = "<=";

	private static Map<String, Integer> registers = new HashMap<>();

	private static List<Instruction> instructions;

	private static int runInstructions()
	{
		int maxHeld = Integer.MIN_VALUE;
		for (Instruction i : instructions)
		{
			if (conditionValid(i.getCondition()))
			{
				int currentValueOfTarget = registers.get(i.getTargetRegister());
				currentValueOfTarget += (i.isIncrease() ? 1 : -1) * i.getValue();
				if (currentValueOfTarget > maxHeld)
				{
					maxHeld = currentValueOfTarget;
				}
				registers.put(i.getTargetRegister(), currentValueOfTarget);
			}
		}
		return maxHeld;
	}

	private static boolean conditionValid(String condition)
	{
		String[] conditionParts = condition.split(" ");
		int registerValue = registers.get(conditionParts[0]);
		int conditionCheckValue = Integer.parseInt(conditionParts[2]);

		switch (conditionParts[1])
		{
		case BIGGER:
			return registerValue > conditionCheckValue;
		case SMALLER:
			return registerValue < conditionCheckValue;
		case SMALLER_EQUAL:
			return registerValue <= conditionCheckValue;
		case BIGGER_EQUAL:
			return registerValue >= conditionCheckValue;
		case EQUAL:
			return registerValue == conditionCheckValue;
		case NOT_EQUAL:
			return registerValue != conditionCheckValue;
		}
		return false;
	}

