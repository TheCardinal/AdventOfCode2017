	public static int countGroups(List<Program> programs)
	{
		int numberOfGroups = 0;
		List<Program> copiedList = new ArrayList<Program>(programs);
		while (!copiedList.isEmpty())
		{
			int initialProgram = programs.get(0).getName();
			List<Program> group = getConnectedPrograms(copiedList, initialProgram);
			copiedList.removeAll(group);
			numberOfGroups++;
		}
		return numberOfGroups;
	}

	public static List<Program> getConnectedPrograms(List<Program> programs, int initialProgram)
	{
		List<Program> connectedPrograms = new ArrayList<>();
		Program program0 = programs.get(initialProgram);
		connectedPrograms.add(program0);
		int addedPrograms = 1;
		while (addedPrograms > 0)
		{
			List<Integer> currNames = connectedPrograms.stream().map(p -> p.getName()).collect(Collectors.toList());

			List<Program> newConnections = programs.stream().filter(p -> p.canCommunicateWith(currNames))
				.filter(p -> !currNames.contains(p.getName()))
				.collect(Collectors.toList());

			addedPrograms = newConnections.size();

			if (!newConnections.isEmpty())
			{
				connectedPrograms.addAll(newConnections);
			}
		}

		System.out.print("Connected: ");
		for (Program p : connectedPrograms)
		{
			System.out.print(p.getName() + ", ");
		}
		System.out.println();

		return connectedPrograms;
	}

